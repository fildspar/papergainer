  /**
   * 获取所有模板列表
   */
  function list() {
    return TEMPLATES;
  }

  /**
   * 根据ID获取模板
   */
  function get(id) {
    return TEMPLATES.find(t => t.id === id) || TEMPLATES[0];
  }

